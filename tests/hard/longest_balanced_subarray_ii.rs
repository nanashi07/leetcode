// 3721. Longest Balanced Subarray II
// https://leetcode.com/problems/longest-balanced-subarray-ii/

struct Solution;

use std::collections::HashSet;

impl Solution {
    pub fn longest_balanced(nums: Vec<i32>) -> i32 {
        let n = nums.len();
        let mut max_len = 0;

        // Pre-allocate sets once and reuse them
        let mut odd = HashSet::with_capacity(128);
        let mut even = HashSet::with_capacity(128);

        for i in 0..n {
            // Early termination: if remaining elements can't beat current max_len
            if (n - i) <= max_len as usize {
                break;
            }

            odd.clear();
            even.clear();
            let mut odd_count = 0;
            let mut even_count = 0;

            for j in i..n {
                let num = nums[j];
                let is_odd = num & 1 == 1;

                if is_odd {
                    if odd.insert(num) {
                        odd_count += 1;
                    }
                } else {
                    if even.insert(num) {
                        even_count += 1;
                    }
                }

                if odd_count == even_count {
                    max_len = max_len.max((j - i + 1) as i32);
                }
            }
        }

        max_len
    }
}

