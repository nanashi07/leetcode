// 3381. Maximum Subarray Sum With Length Divisible by K
// https://leetcode.com/problems/maximum-subarray-sum-with-length-divisible-by-k/

struct Solution;

impl Solution {
    pub fn max_subarray_sum(nums: Vec<i32>, k: i32) -> i64 {
        let k = k as usize;
        let n = nums.len();

        // Track minimum prefix sum for each remainder modulo k
        let mut min_prefix = vec![i64::MAX; k];
        min_prefix[0] = 0; // Empty prefix has sum 0 and remainder 0

        let mut prefix_sum = 0i64;
        let mut max_sum = i64::MIN;

        for i in 0..n {
            prefix_sum += nums[i] as i64;
            let remainder = (i + 1) % k;

            // Check if we can form a valid subarray ending at position i
            if min_prefix[remainder] != i64::MAX {
                max_sum = max_sum.max(prefix_sum - min_prefix[remainder]);
            }

            // Update minimum prefix sum for this remainder
            min_prefix[remainder] = min_prefix[remainder].min(prefix_sum);
        }

        max_sum
    }

    // Time Limit Exceeded
    pub fn _max_subarray_sum(nums: Vec<i32>, k: i32) -> i64 {
        println!("nums: {:?}, k: {k}", &nums);

        let k = k as usize;
        let size = nums.len() / k;
        let mut sum = (0..size)
            .map(|l| {
                nums.iter()
                    .take(k * (l + 1))
                    .map(|&n| n as i64)
                    .sum::<i64>()
            })
            .collect::<Vec<_>>();
        let mut max = sum.clone();

        for i in k..nums.len() {
            for j in 0..size {
                let s = k * (j + 1);
                if i >= s {
                    sum[j] = sum[j] + (nums[i] - nums[i - s]) as i64;
                    max[j] = max[j].max(sum[j]);
                }
            }
        }

        max.into_iter().max().unwrap()
    }
}

